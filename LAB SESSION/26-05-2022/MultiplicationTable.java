package corejava;

// java program to create a multiplication table
		import java.util.Scanner;
		
		public class MultiplicationTable {
			
			// Main driver method
			public static void main(String[] args) {
			
				// initializing value
				
		    int i,n,k;
		      n=3;
			 
		 for (i=0;i<=10;i++) {
			  
			 k=n*i;
			 
			 // print mulitiplication table
				System.out.println(n+"*"+i+"="+k);

		 }
			}

		}

