package corejava;

public class realtionaloperator {

	public static void main(String[] args) {
//variables defination and initialization
		int num1=12,num2=4;
		//is equal to
		System.out.println("num1 == num2="+(num1 == num2));
		//is not equal to
		System.out.println("num1 != num2"+(num1 != num2));
		//greater than
	System.out.println("num1 > num2"+(num1 > num2));
	//less than
	System.out.println("num1 < num2"+(num1 < num2));
	//greater than or equal to
	System.out.println("num1 >=num2"+(num1 >= num2));
	//less than or equal to
	System.out.println("num1 <= num2"+(num1 <= num2));
	}
}

